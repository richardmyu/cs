#include <stdio.h>

/* 用于将输入复制到输出的程序：第一个版本 */

int main()
{
  /* 必须把 c 说明成一个大到足以存放 getchar 函数可能返回的各种值的类型。
  之所以不把 c 说明成 char 类型，
  是因为 c 必须大到除了能存储任何可能的字符外还要能存储文件结束符 EOF。
  因此，把 c 说明成 int 类型的。*/
  int c;

  /* getchar 函数
  在没有输入时返回一个特殊值，这个特殊值不能与任何实际字符相混淆。
  这个值叫做 EOF（End Of File，文件结束）。
  */
  c = getchar();

  /* EOF 是一个在 <stdio.h> 库中定义的整数，
  但其具体的数值是什么并不重要，只要知道它与 char 类型的所有值都不相同就行了。
  可以通过使用符号常量来保证 EOF 在程序中不依赖于特定的数值。
  */
  while (c != EOF)
  {
    putchar(c);
    c = getchar();
  }
}
