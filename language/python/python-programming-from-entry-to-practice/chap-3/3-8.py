travel_sites = [
    'Hogwarts', 'Middle-Earth', 'Salt Lake City', 'Yellowstone National Park',
    'New Zealand'
]

print(travel_sites)
print(sorted(travel_sites))
print(travel_sites)
print(sorted(travel_sites, reverse=True))
print(travel_sites)
print('------------')
travel_sites.reverse()
print(travel_sites)
travel_sites.reverse()
print(travel_sites)
print('------------')
travel_sites.sort()
print(travel_sites)
travel_sites.sort(reverse=True)
print(travel_sites)
